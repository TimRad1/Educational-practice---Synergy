# Кейс-задача № 1
# Дан одномерный массив А размерности N. Найти сумму отрицательных элементов, расположенных между максимальным и минимальным.

#python
def sum_negative_between_min_max(arr):
    if not arr:
        return 0
    min_val = min(arr)
    max_val = max(arr)
    min_index = arr.index(min_val)
    max_index = arr.index(max_val)
    
    start = min(min_index, max_index)
    end = max(min_index, max_index)
    
    subarray = arr[start + 1:end]
    return sum(num for num in subarray if num < 0)

