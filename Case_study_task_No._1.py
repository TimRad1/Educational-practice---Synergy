# Объяснение:
# 1. Проверка на пустой массив: Функция сразу возвращает 0, если массив пуст.
# 2. Поиск минимального и максимального значений: Используются min() и max().
# 3. Индексы элементов: index() возвращает первый встреченный индекс для минимального и максимального значений.
# 4. Определение границ среза: Выбираются меньший и больший индексы, чтобы корректно определить диапазон между ними.
# 5. Фильтрация и суммирование: В срезе между границами суммируются только отрицательные элементы.
